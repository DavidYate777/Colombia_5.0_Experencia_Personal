/* ── Core logic ── */
function applyLang(pageKey, lang) {
  const t = translations[pageKey][lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  document.getElementById('lang-btn').textContent = lang === 'es' ? 'EN' : 'ES';
  localStorage.setItem('co50-lang', lang);
}

function initLang(pageKey) {
  const saved = localStorage.getItem('co50-lang') || 'es';
  applyLang(pageKey, saved);

  document.getElementById('lang-btn').addEventListener('click', () => {
    const current = localStorage.getItem('co50-lang') || 'es';
    applyLang(pageKey, current === 'es' ? 'en' : 'es');
  });
}

/* ── Mobile hamburger ── */
function initNav() {
  const btn   = document.querySelector('.hamburger');
  const links = document.querySelector('.nav-links');
  if (!btn) return;
  btn.addEventListener('click', () => links.classList.toggle('open'));
}

document.addEventListener('DOMContentLoaded', initNav);
