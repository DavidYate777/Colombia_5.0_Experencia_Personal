/* ── Core apply ── */
function applyLang(pageKey, lang) {
  const t = T[pageKey][lang];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  const btn = document.getElementById('lang-btn');
  if (btn) btn.textContent = lang === 'es' ? 'EN' : 'ES';
  localStorage.setItem('co50-lang', lang);
  document.documentElement.lang = lang;
}

function initLang(pageKey) {
  const saved = localStorage.getItem('co50-lang') || 'es';
  applyLang(pageKey, saved);
  const btn = document.getElementById('lang-btn');
  if (btn) btn.addEventListener('click', () => {
    const cur = localStorage.getItem('co50-lang') || 'es';
    applyLang(pageKey, cur === 'es' ? 'en' : 'es');
  });
}

/* ── Mobile nav ── */
function initNav() {
  const btn   = document.querySelector('.hamburger');
  const links = document.querySelector('.nav-links');
  if (!btn) return;
  btn.addEventListener('click', () => links.classList.toggle('open'));
  document.addEventListener('click', e => {
    if (!e.target.closest('nav')) links.classList.remove('open');
  });
}

/* ── Lightbox ── */
function initLightbox() {
  const box = document.getElementById('lightbox');
  const img = document.getElementById('lightbox-img');
  if (!box || !img) return;

  document.querySelectorAll('.gallery img').forEach(i => {
    i.addEventListener('click', () => {
      img.src = i.src;
      box.classList.add('open');
    });
  });
  box.addEventListener('click', () => box.classList.remove('open'));
  document.addEventListener('keydown', e => {
    if (e.key === 'Escape') box.classList.remove('open');
  });
}

document.addEventListener('DOMContentLoaded', () => { initNav(); initLightbox(); });
